# Hello Loop
for hello in range(5):
    print("hello")

# Counting Forwards Loop
for number in range(0,11):
    print(number)

# Counting Backwards Loop
for number in range(10,-1,-1):
    print(number)

# DCC Loop
user_chosen_amount = int(input("How many times would you like to print 'devCodeCamp'? "))
for choice in range(user_chosen_amount):
    print("devCodeCamp")

# Character Loop
team_name = "Packers"
for letter in team_name:
    print(letter)


